#include "libft.h"

int		ft_intlen(long num)
{
	int	i;

	i = 0;
	if (num < 0)
	{
		i++;
		num = num * -1;
	}
	if (num == 0)
		return (1);
	while (num > 0)
	{
		num = num / 10;
		i++;
	}
	return (i);
}
