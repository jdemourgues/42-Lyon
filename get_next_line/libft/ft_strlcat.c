#include "libft.h"

size_t	ft_strlcat(char *dest, const char *src, size_t n)
{
	size_t	i;
	size_t	j;

	i = 0;
	while (dest[i] != '\0' && i < n)
		i++;
	if (n == 0)
		return (ft_strlen((char *)src));
	j = i;
	while (src[i - j] != '\0' && i < n - 1)
	{
		dest[i] = src[i - j];
		i++;
	}
	if (j < n)
		dest[i] = '\0';
	return (j + ft_strlen((char *)src));
}
