int		fib(int n, int f1, int f2)
{
	return ((n == 0) ? f1 : (fib(n - 1, f2, f1 + f2)));
}

int		ft_fibonacci(int index)
{
	if (index >= 0)
		return (fib(index, 0, 1));
	return (-1);
}
